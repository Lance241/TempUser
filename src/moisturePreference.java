public class moisturePreference {
    private int moistureMin;
    private int moistureMax;


    public int getMoistureMin() {
        return moistureMin;
    }

    public void setMoistureMin(int moistureMin) {
        this.moistureMin = moistureMin;
    }

    public int getMoistureMax() {
        return moistureMax;
    }

    public void setMoistureMax(int moistureMax) {
        this.moistureMax = moistureMax;
    }
}
